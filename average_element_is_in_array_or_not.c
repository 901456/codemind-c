#include<stdio.h>
#include<math.h>
int main()
{
    int arr[100],n,i,k,c,sum=0;
    scanf("%d",&n);
    for(i=0;i<n;i++)
    {
        scanf("%d",&arr[i]);
    }
    scanf("%d",&k);
    for(i=0;i<n;i++)
    {
        sum=sum+arr[i];
    }
   k=sum/n;
   i=0;
   for(i=0;i<n;i++)
   {
       if(arr[i]==k)
       {
           c=1;
       }
   }
   if(c==1)
   {
       printf("True");
   }
   else
   {
       printf("False");
   }
}